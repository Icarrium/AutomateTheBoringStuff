# define an empty dict
alien = {}

alien = {'color': 'green', 'points' : '5'}

print(alien['color'])
print(alien['points'])

# print the dictionary
print(alien)

# print dict values
print(alien.values())

# print the dict keys
print(alien.keys())

# adding new key/value pairs
alien['x_coordinate'] = 0
alien['y_coordinate'] = 25

print(alien)